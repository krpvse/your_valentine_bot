async def get_profile_msg(name: str, description: str):
    msg = f'<b>{name}</b>'
    if description:
        msg += f'\n\n{description}'
    return msg


async def get_link_msg(url: str):
    msg = f'Поделись этой ссылкой с друзьями, чтобы они оставили тебе валентинки:\n{url}'
    return msg
